package de.tud.vcd.common.exceptions;

public class XMLCandidateNotFoundException extends Exception {

	public XMLCandidateNotFoundException() {
		
	}

	public XMLCandidateNotFoundException(String message) {
		super(message);
		
	}

	public XMLCandidateNotFoundException(Throwable cause) {
		super(cause);
		
	}

	public XMLCandidateNotFoundException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public XMLCandidateNotFoundException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
