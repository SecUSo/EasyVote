package de.tud.vcd.common.exceptions;

public class BallotCardDesignKeyNotFoundException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BallotCardDesignKeyNotFoundException() {
		
	}

	public BallotCardDesignKeyNotFoundException(String message) {
		super(message);
		
	}

	public BallotCardDesignKeyNotFoundException(Throwable cause) {
		super(cause);
		
	}

	public BallotCardDesignKeyNotFoundException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public BallotCardDesignKeyNotFoundException(String message,
			Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
