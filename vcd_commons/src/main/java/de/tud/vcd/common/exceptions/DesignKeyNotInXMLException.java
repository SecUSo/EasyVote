package de.tud.vcd.common.exceptions;

public class DesignKeyNotInXMLException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DesignKeyNotInXMLException() {
		
	}

	public DesignKeyNotInXMLException(String message) {
		super(message);
		
	}

	public DesignKeyNotInXMLException(Throwable cause) {
		super(cause);
		
	}

	public DesignKeyNotInXMLException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public DesignKeyNotInXMLException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
